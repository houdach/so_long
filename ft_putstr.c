#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	if (s == NULL)
	{
		ft_putstr("(null)", len);
		return ;
	}	
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i], len);
		i++;
	}
}
